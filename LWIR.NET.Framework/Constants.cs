using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace LWIR.NET.Framework
{
    /// <summary>
    /// 系统常量
    /// </summary>
    public class Constants
    {
        /// <summary>
        /// 1 KB 的常量字节大小
        /// </summary>
        public const ulong KB = 1024;

        /// <summary>
        /// 1 MB 的常量字节大小
        /// </summary>
        public const ulong MB = 1024 * KB;

        /// <summary>
        /// 1GB 的常量字节大小
        /// </summary>
        public const ulong GB = 1024 * MB;
    }
}
